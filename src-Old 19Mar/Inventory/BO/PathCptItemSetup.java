package Inventory.BO;

/**
 *
 * @author raheelansari
 */
public class PathCptItemSetup {
    
    private String cptId = "";
    private String cptName = "";
    private String specimenId = "";
    private String tubeCodeId = "";
    private String itemId = "";
    private String itemName = "";
    private String quantity = "";

    public String getCptId() {
        return cptId;
    }

    public void setCptId(String cptId) {
        this.cptId = cptId;
    }

    public String getCptName() {
        return cptName;
    }

    public void setCptName(String cptName) {
        this.cptName = cptName;
    }

    public String getSpecimenId() {
        return specimenId;
    }

    public void setSpecimenId(String specimenId) {
        this.specimenId = specimenId;
    }

    public String getTubeCodeId() {
        return tubeCodeId;
    }

    public void setTubeCodeId(String tubeCodeId) {
        this.tubeCodeId = tubeCodeId;
    }

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }
    
    
}
